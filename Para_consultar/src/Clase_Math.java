public class Clase_Math{
	public static void main(String[] args) {
		
		double num;
		
		double PI = Math.PI;
		
		//POTENCIA
		num = Math.pow(8, 2);
		System.out.println(num);
		
		//RAIZ
		num = Math.sqrt(64);
		System.out.println(num);
		
		//REDONDEAR
		num = Math.round(45.49);
		System.out.println(num);
		
		//El ángulo está en radianes
		double seno = Math.sin(Math.PI/2);
		System.out.println(seno);
		
		
	}
}